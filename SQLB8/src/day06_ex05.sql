COMMENT ON TABLE person_discounts IS 
'Таблица персональных скидок для клиентов в пиццериях.
Содержит информацию о размере скидки, предоставляемой конкретному клиенту в конкретной пиццерии.';

COMMENT ON COLUMN person_discounts.id IS 
'Уникальный идентификатор записи о скидке.
Первичный ключ таблицы. Используется для однозначной идентификации каждой записи.';

COMMENT ON COLUMN person_discounts.person_id IS 
'Идентификатор клиента.
Внешний ключ, ссылается на таблицу person(id).
Определяет, для какого клиента действует скидка.';

COMMENT ON COLUMN person_discounts.pizzeria_id IS 
'Идентификатор пиццерии.
Внешний ключ, ссылается на таблицу pizzeria(id).
Определяет, в какой пиццерии действует скидка.';

COMMENT ON COLUMN person_discounts.discount IS 
'Размер скидки в процентах.
Используется для расчета итоговой стоимости заказа со скидкой.';

